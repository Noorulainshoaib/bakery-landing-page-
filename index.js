    const textToType = "Delicious Bakery Items for Everyone";
    let currentText = "";
    let textIndex = 0;

    function typeText() {
        if (textIndex < textToType.length) {
            currentText += textToType.charAt(textIndex);
            typingH2.innerText = currentText;
            textIndex++;
            setTimeout(typeText, 100); // Adjust the typing speed here (100ms delay in this example)
        }
    }

    // Call the typing animation function
    typeText();
